package com.klink.domain;

public class Job {

	private long id;
	
	private String title;
	
	public void setTitle(String title) {
		this.title = title;
	}

	public String getTitle() {
		return title;
	}

	public void setId(long id) {
		this.id = id;
	}

	public long getId() {
		return id;
	}

		
}
